            <p class="text-justify">La formation permet aux étudiants d’acquérir les diplômes de Licence en TECHNOLOGIE DE L’INFORMATIQUE ET DE LA TELECOMMUNICATION et ce suivant le système LMD actuellement appliqué à Madagascar.</p>

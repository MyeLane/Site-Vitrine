            <h3 align="center" class="color pb-4 col-12 col-sm-12 col-md-12 col-lg-12 title mb-4">FORMATION PROFESSIONNELLE FILIERE IMAGE</h3>
            <h4 class="mb-2">Présentation</h4>
            <p class="text-justify">La filière Image a pour but de former des techniciens du métier de l’image. Elle offre une formation essentiellement technique, basé sur les connaissances des bases théoriques et pratiques des techniques et des outils relatifs aux traitements de l’image. La formation octroyée sur un moyen terme, permet aux étudiants d’être directement opérationnel. Face à un environnement professionnel en quête de polyvalence, la formation en Image, se veut être complète, en intégrant tous les modules utiles à l’étudiants. Les étudiants seront aptes à réaliser à traiter les images fixes tels que les affiches, flyers, brochures…en passant par les images mobiles comme les clips, les publicités, les films, documentaires ….ainsi que la production 3D.</p>
            <h4 class="mb-2">Programme</h4>
            <p class="text-justify">D’une durée de 08 Mois, la formation est composée des modules suivantes :</p>
            <ul>
                <li>Conception cinématographique</li>
                <li>Photographie</li>
                <li>Prise de vue</li>
                <li>Dessin</li>
                <li>PAO</li>
                <li>Post production</li>
                <li>3D</li>
            </ul>
            <h4 class="mb-2">Admissibilité</h4>
            <p class="text-justify">La formation est ouverte à toutes personnes, sans limite d’âge, ayant ou non le baccalauréat mais jouissant d’un niveau terminal minimum.</p>
            <h4 class="mb-2">Prérequis</h4>
            <p>Connaissances de base en informatique (structure de l’ordinateur…)</p>
            <h4 class="mb-2">Sanction</h4>
            <p class="text-justify">Un examen officiel composé de partie théorique et pratique, préparé et dirigé par le Ministère de l’Enseignement Technique et de la Formation Professionnelle (METFP) permet d’obtenir un Certificat de Fin de Formation, Spécialité : IMAGE (Equivalent BACCALAUREAT / CATEGORIE III FOP)</p>
            <h4 class="mb-2">Débouchés</h4>
            <p class="text-justify">Technicien en audiovisuel : Monteur , Cadreur, Infographiste, Cameraman etc.… au sein des structures tels que les agences de communication, télévision, cinéma, imprimerie, et toutes autres entreprises.</p>
